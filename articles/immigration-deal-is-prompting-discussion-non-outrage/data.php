<p>The immigration issue was never easy. But it hasn’t always been this confusing.</p>

<p>For much of the 1990s, the two parties essentially agreed on the issue of immigration. But in the last 13 years, attitudes toward immigrants have forked dramatically between the two parties. Today, eight in 10 Democrats and Democratic-leaning voters say immigrants strengthen the country, twice the share of Republicans.</p>

<p>This divide has appeared in Congress, as well.</p>

<p>During the latest immigration hearings in the Senate, Senators Orrin Hatch (R – UT) and Sherrod Brown (D – OH) did not agree on how to move forward. Sen. Brown stated Sen. Hatch was “losing focus” and ignoring the benefits immigrants bring to the United States. </p>

<p>Sen. Hatch responded, “Immigration is on the agenda. You guys know we will get to it soon.” No agreement came out of the discussion.</p>

<p>Other senators, including Sens. John McCain (R-Ariz.) and Christopher Coons (D-Del.), are working on a bipartisan immigration deal, but there is no guarantee other members of their parties will support it.</p>

<p>Democrats want protections for Dreamers – undocumented immigrants brought to the U.S. as children. “A bill with any hope of passing must allow Dreamers to stay here and become citizens,” said Senate Minority Leader Chuck Schumer.</p>

<p>Clearly, immigration is not a monolithic issue; there are at least three immigration questions: How should the United States treat illegal immigrants, especially those brought to the country as children? Should overall immigration levels be reduced, increased, or neither? And how should the U.S. prioritize the various groups seeking entry to the country?</p>

<p>It is possible that most voters don’t look at immigration this specifically, and don’t think too much about the answers to each question. This makes Congress even less motivated to address the issue.</p>';

